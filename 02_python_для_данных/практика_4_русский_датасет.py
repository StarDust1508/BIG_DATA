def генерировать_телефон() -> str:
    """Возвращает телефон в одном из 5 форматов — для тренировки очистки.

    Российский мобильный = 10 цифр после кода страны:
      9XX XXXXXXX  → начинается с 9.
    """
    n = random.choices([0, 1, 2, 3, 4, 5], weights=[0.4, 0.2, 0.15, 0.10, 0.10, 0.05])[0]
    # 10 цифр: 9 + 9 цифр
    digits = "9" + "".join(random.choices("0123456789", k=9))   # длина 10
    if n == 0:
        return f"+7{digits}"
    if n == 1:
        return f"8{digits}"
    if n == 2:
        return f"+7 ({digits[:3]}) {digits[3:6]}-{digits[6:8]}-{digits[8:]}"
    if n == 3:
        return f"8-{digits[:3]}-{digits[3:6]}-{digits[6:8]}-{digits[8:]}"
    if n == 4:
        return f"+7{digits[0:3]} {digits[3:6]} {digits[6:8]} {digits[8:]}"
    return ""  # 5% пропусков


def генерировать_инн_физлица() -> str:
    """12-значный ИНН физлица."""
    return "".join(random.choices("0123456789", k=12))


def генерировать_снилс() -> str:
    """СНИЛС: XXX-XXX-XXX YY."""
    return (
        f"{random.randint(100,999)}-"
        f"{random.randint(100,999)}-"
        f"{random.randint(100,999)} "
        f"{random.randint(10,99)}"
    )


def генерировать_паспорт() -> str:
    """Серия и номер паспорта РФ."""
    return f"{random.randint(1000, 9999)} {random.randint(100000, 999999)}"


def сгенерировать_сотрудника(id_: int) -> dict:
    is_female = random.random() < 0.5
    if is_female:
        фам = random.choice(ФАМИЛИИ_М) + "а"
        имя = random.choice(ИМЕНА_Ж)
        отч = random.choice(ОТЧЕСТВА_Ж)
    else:
        фам = random.choice(ФАМИЛИИ_М)
        имя = random.choice(ИМЕНА_М)
        отч = random.choice(ОТЧЕСТВА_М)

    # 5% — небольшие огрехи в имени (двойной пробел, табы)
    fio = f"{фам} {имя} {отч}"
    if random.random() < 0.05:
        fio = fio.replace(" ", "  ", 1)
    if random.random() < 0.02:
        fio = fio.lower()  # «иванов иван иванович»

    дата_рождения = date(1965, 1, 1) + timedelta(days=random.randint(0, 365 * 45))
    дата_приёма = date(2010, 1, 1) + timedelta(days=random.randint(0, 365 * 16))

    зарплата = random.randint(35_000, 350_000)
    if random.random() < 0.02:
        зарплата *= 10  # «звёздный» специалист — анти-аномалия

    город = random.choice(ГОРОДА)
    if random.random() < 0.03:
        город = ""  # пропуски

    return {
        "id": id_,
        "ФИО": fio,
        "пол": "Ж" if is_female else "М",
        "дата_рождения": дата_рождения.isoformat(),
        "ИНН": генерировать_инн_физлица(),
        "СНИЛС": генерировать_снилс(),
        "паспорт": генерировать_паспорт(),
        "телефон": генерировать_телефон(),
        "город": город,
        "должность": random.choice(ДОЛЖНОСТИ),
        "отдел": random.choice(ОТДЕЛЫ),
        "работодатель": random.choice(КОМПАНИИ),
        "дата_приёма": дата_приёма.isoformat(),
        "зарплата": зарплата,
    }


