import hashlib
import os
import re
from datetime import date
from pathlib import Path

import pandas as pd


ROOT = Path(__file__).resolve().parent.parent
ВХОД = ROOT / "datasets" / "сотрудники_рф.csv"
ВЫХОД = ROOT / "datasets" / "сотрудники_обезличенные.parquet"


def нормализовать_фио(fio: str) -> str:
    """Чистит ФИО: убирает лишние пробелы, приводит к Title Case."""
    if pd.isna(fio):
        return None
    fio = re.sub(r"\s+", " ", str(fio)).strip()
    # Каждое слово с заглавной
    return " ".join(p.capitalize() for p in fio.split())


def нормализовать_телефон(p: str) -> str | None:
    """Любой формат → +7XXXXXXXXXX. None если нельзя нормализовать."""
    if pd.isna(p):
        return None
    цифры = re.sub(r"\D", "", str(p))
    if not цифры:
        return None
    if цифры[0] == "8" and len(цифры) == 11:
        цифры = "7" + цифры[1:]
    if not цифры.startswith("7"):
        цифры = "7" + цифры
    if len(цифры) != 11:
        return None
    return "+" + цифры


def проверить_инн(инн: str) -> bool:
    """Базовая проверка: 12 цифр для физлица."""
    if pd.isna(инн):
        return False
    s = str(инн).strip()
    return len(s) == 12 and s.isdigit()


def возрастная_группа(дата_рождения: str) -> str:
    if pd.isna(дата_рождения):
        return "неизвестно"
    bd = pd.to_datetime(дата_рождения).date()
    возраст = (date.today() - bd).days // 365
    if возраст < 30:
        return "до 30"
    if возраст < 40:
        return "30-39"
    if возраст < 50:
        return "40-49"
    if возраст < 60:
        return "50-59"
    return "60+"


def псевдонимизировать_id(id_: int, соль: str) -> str:
    return hashlib.sha256((соль + str(id_)).encode()).hexdigest()[:16]


def главная() -> None:
    if not ВХОД.exists():
        print("❌ Сначала запустите практика_4_русский_датасет.py")
        return

    # ── TODO 1: загрузка ────────────────────────────────────────────
    print("=== 1. Загрузка ===")
    df = pd.read_csv(ВХОД)
    print(f"Загружено: {len(df):,} строк, {df.shape[1]} колонок")
    print("Колонки:", list(df.columns))
    print("\nТипы данных:")
    print(df.dtypes)

    # ── TODO 2: чистка ФИО ──────────────────────────────────────────
    print("\n=== 2. Очистка ФИО ===")
    подозр = df[df["ФИО"].str.contains("  ", na=False) | (df["ФИО"] != df["ФИО"].str.title())].shape[0]
    print(f"Записей с подозрительным форматом ФИО: {подозр}")
    df["ФИО"] = df["ФИО"].apply(нормализовать_фио)
    print("После нормализации первые 5:")
    print(df["ФИО"].head().tolist())

    # ── TODO 3: телефоны ───────────────────────────────────────────
    print("\n=== 3. Нормализация телефонов ===")
    df["телефон_норм"] = df["телефон"].apply(нормализовать_телефон)
    bad = df["телефон_норм"].isna().sum()
    print(f"Не удалось нормализовать: {bad} ({bad/len(df)*100:.1f}%)")
    print("Пример нормализованных:")
    print(df[["телефон", "телефон_норм"]].head())

    # ── TODO 4: проверка ИНН ───────────────────────────────────────
    print("\n=== 4. Проверка ИНН ===")
    df["инн_валиден"] = df["ИНН"].apply(проверить_инн)
    invalid = (~df["инн_валиден"]).sum()
    print(f"Невалидных ИНН: {invalid}")

    # ── TODO 5: аналитика ──────────────────────────────────────────
    print("\n=== 5. Аналитика ===")
    print("5a) Топ-5 городов:")
    print(df[df["город"] != ""]["город"].value_counts().head())

    print("\n5b) Средняя зарплата по отделам (топ-5):")
    print(df.groupby("отдел")["зарплата"].mean().round(0).sort_values(ascending=False).head())

    print("\n5c) Доля женщин на руководящих должностях:")
    руковод = df[df["должность"].str.contains("Руководитель|Директор", regex=True, na=False)]
    if len(руковод):
        доля = (руковод["пол"] == "Ж").mean() * 100
        print(f"   Всего на руководящих: {len(руковод)}")
        print(f"   Из них женщин: {доля:.1f}%")

    print("\n5d) Распределение по возрастным когортам:")
    df["возрастная_группа"] = df["дата_рождения"].apply(возрастная_группа)
    print(df["возрастная_группа"].value_counts().sort_index())

    # ── TODO 6: псевдонимизация ────────────────────────────────────
    print("\n=== 6. Псевдонимизация ===")
    соль = os.environ.get("PSEUDO_SALT")
    if not соль:
        print("⚠️  PSEUDO_SALT не задана. Использую 'dev-salt' (только для учёбы!)")
        соль = "dev-salt-CHANGE-ME"

    обезл = df.copy()
    обезл["токен"] = обезл["id"].apply(lambda x: псевдонимизировать_id(x, соль))
    обезл = обезл.drop(columns=["id", "ФИО", "ИНН", "СНИЛС",
                                  "паспорт", "телефон", "телефон_норм",
                                  "инн_валиден"])
    # Записать в Parquet
    try:
        обезл.to_parquet(ВЫХОД, compression="snappy")
        print(f"💾 Обезличенный датасет: {ВЫХОД}")
    except Exception as e:
        print(f"⚠️  Не записал Parquet ({e}). pyarrow не установлен?")

    # ── TODO 7: k-анонимность ──────────────────────────────────────
    print("\n=== 7. K-анонимность по (город, отдел, возрастная_группа) ===")
    группы = обезл.groupby(["город", "отдел", "возрастная_группа"]).size()
    проблемных = (группы < 3).sum()
    записей_в_проблемных = группы[группы < 3].sum()
    print(f"Всего комбинаций: {len(группы)}")
    print(f"Групп с k<3 (нарушают анонимность): {проблемных}")
    print(f"Записей в проблемных группах: {записей_в_проблемных}")

    # ── TODO 8: юридический разбор ─────────────────────────────────
    print(
        """
=== 8. Юридический разбор ===

а) Это ПДн по 152-ФЗ?
   ✅ ДА. ФИО + ИНН + СНИЛС + паспорт + дата рождения = ПДн.
   Категория: общие ПДн (нет специальных — расы, политики, здоровья).

б) Прямые идентификаторы (видны субъект):
   - ФИО, ИНН, СНИЛС, паспорт, телефон, дата рождения (узкая когорта).

в) Квази-идентификаторы (не идентифицируют сами, но в комбинации могут):
   - Город + Отдел + Должность + Зарплата (особенно если зарплата высокая).
   - Возрастная группа + Город + Пол.

г) Облако ЕС и GDPR:
   - Передача персональных данных за пределы РФ — требует согласия субъекта
     или другого правового основания (242-ФЗ + GDPR).
   - Первичная запись ПДн должна быть в РФ (242-ФЗ).
   - Облако ЕС → передача → нужно согласие + DPA + Standard Contractual Clauses.
   - Решение: ОБЕЗЛИЧИТЬ до того, как данные пойдут за границу.
        """
    )


if __name__ == "__main__":
    главная()
